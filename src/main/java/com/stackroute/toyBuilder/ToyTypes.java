package com.stackroute.toyBuilder;

public class ToyTypes {
//    declaration of variables
    private String staticToys;
    private String movableToys;
    private String ToysThatfly;
    private String ToysThatFlyAndMove;
//    getters and setters

    public String getStaticToys() {

        return staticToys;
    }

    public void setStaticToys(String staticToys) {

        this.staticToys = staticToys;
    }

    public String getMovableToys() {
        return movableToys;
    }

    public void setMovableToys(String movableToys) {
        this.movableToys = movableToys;
    }

    public String getToysThatfly() {
        return ToysThatfly;
    }

    public void setToysThatfly(String toysThatfly) {
        ToysThatfly = toysThatfly;
    }

    public String getToysThatFlyAndMove() {
        return ToysThatFlyAndMove;
    }

    public void setToysThatFlyAndMove(String toysThatFlyAndMove) {
        ToysThatFlyAndMove = toysThatFlyAndMove;
    }

}

